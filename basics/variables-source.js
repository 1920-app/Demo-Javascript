// Une variable déclarée avec 'var' a une portée de fonction
var uneVariable = "Bonjour";

// Une variable déclarée avec 'let' a une portée de bloc
let uneAutreVariable = "Au revoir";

// Une variable déclarée avec 'const' a une portée de bloc
// Pour la rendre globale, il faut la déclarer en dehors de toute fonction
const uneDerniereVariable = "À bientôt";

